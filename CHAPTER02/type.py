a = "76.9"
t = type(a) # this will return <class 'str'> because a is a string
# the type() function is used to determine the data type of a variable in python. In this case, since a is assigned a string value, the type of a will be <class 'str'>.

print(t) # this will print <class 'str'>



# converting string to float
b = "53.987"
c = float(b) # this will convert the string b to a float and assign it to c
t = type(c) # this will return <class 'float'> because c is now a float after conversion
print(t) # this will print <class 'float'>

# float() function is used to convert a string that represents a number into a float data type. In this case, the string "53.987" is converted to the float 53.987, and the type of c will be <class 'float'>.
