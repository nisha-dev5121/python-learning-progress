for i in range(0, 100, 4):
    print(i)

#it started with 0 till 100 and skipped every other 4 number.
#range function is used to generate a sequence of numbers. 
# It takes three arguments: start, stop, and step. 
# The start argument is the number from which the sequence will start, 
# the stop argument is the number at which the sequence will stop (but it will not include this number), 
# and the step argument is the number by which the sequence will be incremented (or decremented if it is negative). 
# In this case, we have used a step of 4, so it will generate a sequence of numbers starting from 0 and incrementing by 4 until it reaches 100 (but it will not include 100). 